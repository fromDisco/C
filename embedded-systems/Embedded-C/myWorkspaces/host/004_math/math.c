int math_add(int num_a, int num_b)
{
	return num_a + num_b;
}

int math_sub(int num_a, int num_b)
{
	return num_a - num_b;
}

long long int math_mul(int num_a, int num_b)
{
	return (long long int)num_a * num_b;
}

float math_div(int num_a, int num_b)
{
	return (float)num_a / num_b;
}
